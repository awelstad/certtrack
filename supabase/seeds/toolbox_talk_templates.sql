('Grounding, Bonding, and Static Electricity', 'Electrical Safety',
'Grounding and bonding are critical safety practices for preventing electrical shock and fires caused by static electricity and stray electrical current. These principles are especially important when working with flammable liquids, fuel systems, and electrical equipment.

THE DIFFERENCE BETWEEN GROUNDING AND BONDING
BONDING: Connecting two or more conductive objects together to equalize their electrical potential. If two metal containers are at different electrical potentials, a spark can jump between them — potentially igniting flammable vapors.
GROUNDING: Connecting a conductive object to the earth. This provides a path for stray current to dissipate safely into the ground rather than through a person.

WHY IT MATTERS WITH FLAMMABLE LIQUIDS
When flammable liquids are transferred between containers, friction generates static electricity. If the containers are not bonded together, a static spark can ignite the vapor above the liquid. Always bond containers together before transferring flammable liquids, and ground at least one container to earth.

