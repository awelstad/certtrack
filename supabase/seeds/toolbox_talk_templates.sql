('Lock Out / Tag Out (LOTO)', 'Electrical & Energy Safety',
'LOTO (Lock Out / Tag Out) is the procedure used to ensure that hazardous energy is completely shut off and cannot be released while someone is working on or near equipment. Failure to follow LOTO is one of the most common causes of serious injury and death in construction.

WHY LOTO MATTERS
Unexpected energization, start-up, or release of stored energy can cause electrocution, burns, crushing, amputation, and death. LOTO ensures the energy stays off — full stop.

THE 6-STEP LOTO PROCEDURE
1. Notify — Inform all affected employees that the equipment is being shut down.
2. Identify — Locate all energy sources (electrical, pneumatic, hydraulic, gravity, thermal, chemical).
3. Shut down — Follow normal stopping procedures to shut equipment down properly.
4. Isolate — Disconnect from energy source using the energy isolating device (breaker, valve, disconnect).
5. Lock and tag — Apply your personal lock and tag to the isolating device. Your lock, your key — never share.
6. Verify — Attempt to start the equipment. Confirm all stored energy has been released (bleed pressure, block gravity loads).

TYPES OF ENERGY TO CONSIDER
Do not forget non-electrical energy: pressurized air and hydraulic lines, spring tension, gravity (suspended loads), and thermal energy (hot pipes, steam).

LOCK TYPES
• Personal lock: One person, one key. Never let someone else use your lock.
• Hasp: Used when multiple workers need their own locks on the same point.
• Tag alone is never enough — a tag can be removed or ignored. Always lock out.

AFTER WORK IS COMPLETE
Only the person who placed the lock removes it. Before re-energizing: confirm all workers are clear, tools and materials are removed, guards are replaced, and affected employees are notified.

